class MonsterDefinitions(Defintions):
  """
    this class will function as a sort of pointer to a monster in the 
    allmonsters dict. It's needed in case, I need to change one of these
    properties globally. Each model of monster will have one of these.
  """
  global allMonsters
  _indexes = {
      MonsterDefinitionFields.MECH:
        allMonsters[ZoneDefinitionFields.ALL][MonsterDefinitionFields.MECH],
      MonsterDefinitionFields.PIRATES:
        allMonsters[ZoneDefinitionFields.ALL][MonsterDefinitionFields.PIRATES],
      MonsterDefinitionFields.M_SCOUT:
        allMonsters[ZoneDefinitionFields.ALL][MonsterDefinitionFields.M_SCOUT],
      MonsterDefinitionFields.SMALL_ASTEROID:
        allMonsters[ZoneDefinitionFields.ALL][MonsterDefinitionFields.SMALL_ASTEROID],
      MonsterDefinitionFields.SPACE_SLIME:
        allMonsters[ZoneDefinitionFields.ALL][MonsterDefinitionFields.SPACE_SLIME],
      MonsterDefinitionFields.SPACEMAN:
        allMonsters[ZoneDefinitionFields.ALL][MonsterDefinitionFields.SPACEMAN],
      MonsterDefinitionFields.POISON_CLOUD:
        allMonsters[ZoneDefinitionFields.GAS][MonsterDefinitionFields.POISON_CLOUD],
      MonsterDefinitionFields.CLOUD_FORTRESS:
        allMonsters[ZoneDefinitionFields.GAS][MonsterDefinitionFields.CLOUD_FORTRESS],
      MonsterDefinitionFields.DREAD_PIRATES:
        allMonsters[ZoneDefinitionFields.EMPTY_SPACE][MonsterDefinitionFields.DREAD_PIRATES],
      MonsterDefinitionFields.GRAVITY_HOLE:
        allMonsters[ZoneDefinitionFields.EMPTY_SPACE][MonsterDefinitionFields.GRAVITY_HOLE],
      MonsterDefinitionFields.ZOMBIE_FREIGHTER:
        allMonsters[ZoneDefinitionFields.EMPTY_SPACE][MonsterDefinitionFields.ZOMBIE_FREIGHTER],
      MonsterDefinitionFields.SPACE_PARASITES:
        allMonsters[ZoneDefinitionFields.EMPTY_SPACE][MonsterDefinitionFields.SPACE_PARASITES],
      MonsterDefinitionFields.BIG_ASTEROID:
        allMonsters[ZoneDefinitionFields.ASTEROID_FIELD][MonsterDefinitionFields.BIG_ASTEROID],
      MonsterDefinitionFields.MINER:
        allMonsters[ZoneDefinitionFields.ASTEROID_FIELD][MonsterDefinitionFields.MINER],
      MonsterDefinitionFields.AMBUSH_PIRATES:
        allMonsters[ZoneDefinitionFields.ASTEROID_FIELD][MonsterDefinitionFields.AMBUSH_PIRATES],
      MonsterDefinitionFields.SPACE_GOLEM:
        allMonsters[ZoneDefinitionFields.ASTEROID_FIELD][MonsterDefinitionFields.SPACE_GOLEM],
      MonsterDefinitionFields.MOTHS:
        allMonsters[ZoneDefinitionFields.ASTEROID_FIELD][MonsterDefinitionFields.MOTHS],
    }

  def get_baseXpReward(self):
    return self._indexes[self._key][MonsterFields.BASE_XP_REWARD]

  def get_base_attackLvl(self):
    return self._indexes[self._key][MonsterFields.ATTACK_LVL]

  def get_treasureDrops(self):
    return self._indexes[self._key][MonsterFields.TRASURE_DROPS]

  def get_dropRate(self):
    return self._indexes[self._key][MonsterFields.TRASURE_DROP_RATE]
